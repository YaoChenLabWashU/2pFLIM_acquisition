% --- Executes on button press in equalizeNonBold.
function equalizeNonBold_Callback(hObject, eventdata, handles)
% hObject    handle to equalizeNonBold (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global state
if numel(state.spc.acq.modulesAvail)>1
    currentBoard=get(handles.figure1,'UserData');
    otherModules=state.spc.acq.modulesAvail(state.spc.acq.modulesAvail~=(currentBoard-1));
    % we assume that parameters are already current for the currentBoard
    for m=otherModules
        saveSPC=state.spc.acq.SPCdata{m+1};  % copy the old values
        state.spc.acq.SPCdata{m+1}=state.spc.acq.SPCdata{currentBoard};
        % keep the 'bold' values from the old settings for each board
        %   currently: cfd_limit_low, cfd_zc_level, scan_rout_x
        state.spc.acq.SPCdata{m+1}.cfd_limit_low=saveSPC.cfd_limit_low;
        state.spc.acq.SPCdata{m+1}.cfd_zc_level=saveSPC.cfd_zc_level;
        state.spc.acq.SPCdata{m+1}.scan_rout_x=saveSPC.scan_rout_x;
        state.spc.acq.SPCdata{m+1}.tac_offset=saveSPC.tac_offset;
        FLIM_setParameters(m);
        FLIM_getParameters(m);
    end
end
